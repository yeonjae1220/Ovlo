export function resolveUiLang(input: string | null | undefined): UiLanguage {
  if (!input) return 'en'
  const code = input.slice(0, 2).toLowerCase()
  return (SUPPORTED_UI_LANGUAGES as readonly string[]).includes(code)
    ? (code as UiLanguage)
    : 'en'
}

